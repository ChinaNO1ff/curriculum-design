package com.lzw.dao.model;

public class TbType {
	private String type;
	private String name;
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String toString() {
		return "TbType [type=" + type + ", name=" + name + "]";
	}
}
